package ccproxy;

public class WiadomoscOdAsteriska {

  String wiadomosc;
  int numer;
  boolean wyslana = false;


  public String getWiadomosc() {
    return wiadomosc;
  }


  public void setWiadomosc(String wiadomosc) {
    this.wiadomosc = wiadomosc;
  }


  public int getNumer() {
    return numer;
  }


  public void setNumer(int numer) {
    this.numer = numer;
  }


  public boolean isWyslana() {
    return wyslana;
  }


  public void setWyslana(boolean wyslana) {
    this.wyslana = wyslana;
  }
}
