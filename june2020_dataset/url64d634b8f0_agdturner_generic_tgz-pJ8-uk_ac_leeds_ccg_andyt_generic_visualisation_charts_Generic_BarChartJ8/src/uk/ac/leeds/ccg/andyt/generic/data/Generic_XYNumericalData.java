package uk.ac.leeds.ccg.andyt.generic.data;

import java.math.BigDecimal;

public class Generic_XYNumericalData {

    public BigDecimal x;
    public BigDecimal y;

    public Generic_XYNumericalData() {
    }

    public Generic_XYNumericalData(
            BigDecimal x,
            BigDecimal y) {
        this.x = x;
        this.y = y;
    }

    public BigDecimal getX() {
        return x;
    }

    public void setX(BigDecimal x) {
        this.x = x;
    }

    public BigDecimal getY() {
        return y;
    }

    public void setY(BigDecimal y) {
        this.y = y;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Generic_XYNumericalData other = (Generic_XYNumericalData) obj;
        if (this.x != other.x && (this.x == null || !this.x.equals(other.x))) {
            return false;
        }
        if (this.y != other.y && (this.y == null || !this.y.equals(other.y))) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 19 * hash + (this.x != null ? this.x.hashCode() : 0);
        hash = 19 * hash + (this.y != null ? this.y.hashCode() : 0);
        return hash;
    }
}
