package malictus.klang.chunk;

import java.io.IOException;
import java.io.RandomAccessFile;
import malictus.klang.file.*;

/* This chunk has a name and works in the normal way, but we don't know how to parse it.
 * It may still have an informative description though.
 */
public class UnknownChunk extends Chunk {
	
	public UnknownChunk(KlangFile parentFile, long startpos, long endpos, long datastartpos, long dataendpos, int chunkType, String chunkName, ContainerChunk parentChunk, RandomAccessFile raf) throws IOException {
		super(parentFile, startpos, endpos, datastartpos, dataendpos, chunkType, chunkName, parentChunk, raf);
	}
	
}
