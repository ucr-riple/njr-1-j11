package patterns.gof.structural.proxy;

public class Matematika {
	public int slozhit(int a, int b) {
		return a + b;
	}
	
	public int vychest(int a, int b) {
		return a - b;
	}
	
	public int peremnozhit(int a, int b) {
		return a * b;
	}
	
	public float razdelit(int a, int b) {
		return a / b;
	}
}